int		ft_atoi(const char *str)
{
	int	res;
	int	min;

	res = 0;
	min = 0;
	while ((*str >= 1 && *str <= 19) || *str == 32)
		++str;
	if (*str == '+' || *str == '-')
		if (*(str++) == '-')
			++min;
	while (*str >= '0' && *str <= '9')
		res = res * 10 + (*(str++) - 48);
	return ((min ? -res : res));
}
