#include <stdio.h>

int					ft_is_power_of_2(unsigned int n)
{	
	unsigned int	p;

	if (n > 0 && (p = 1))
	{
		while (p < n)
			p = p * 2;
		return ((p == n ? 1 : 0));
	}
	return ((n == 0 ? 1 : 0));
}
